package assignments1;

public class MultiplicationOfFirst10Numbers {

	    public static void main(String[] args) {
	        int num = 10, count = 1, total = 1;          

	        	       while(count <= num)
	        	       {
	        	           total = total * count;
	        	           count++;
	        	       }

	        	       System.out.println("Multiplication of first 10 natural numbers is: "+total);
	        	    }
	        	}

	        	
